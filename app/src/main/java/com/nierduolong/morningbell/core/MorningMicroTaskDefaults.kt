    /** 用种子稳定选一句，同一天同任务多次点完成也一致；换任务后种子会变 */
    fun pickPraise(seed: Long): String {
        if (praiseLines.isEmpty()) return "今天已经启动成功。"
        val idx = ((seed * 31L) xor (seed ushr 16)).and(0x7FFFFFFF) % praiseLines.size
        return praiseLines[idx.toInt()]
    }

    fun pickTaskFromPool(
        pool: List<String>,
        seed: Long,
    ): String {
        if (pool.isEmpty()) return "喝一杯水"
        val idx = Random(seed).nextInt(pool.size)
        return pool[idx]
    }
